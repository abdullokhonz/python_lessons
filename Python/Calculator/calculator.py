x = input("What to do? +, -, *, / : ");
num1 = float(input("Enter first number: "));
num2 = float(input("Enter second number: "));
if (x == "+"):
    result = num1 + num2;
    print(result);
if (x == "-"):
    result = num1 - num2;
    print(result);
if (x == "*"):
    result = num1 * num2;
    print(result);
if (x == "/"):
    result = num1 / num2;
    print(result);
