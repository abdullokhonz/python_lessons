print ("Begin3◦. Даны стороны прямоугольника a и b. Найти его площадь S = a·b и периметр P = 2·(a + b).")

a = int (input ("Введите сторону прямоугольника: "))
b = int (input ("ВВедите другую сторону прямоугольника: "))
s = a * b
p = 2 * (a + b)
print ("Площадь прямоугольника: " + str(s))
print ("Периметр прямоугольника: " + str(p))